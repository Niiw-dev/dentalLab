function openInteractiveWindow(url) {
    // Define las dimensiones y características de la ventana emergente
    const width = 450; // Ajusta el ancho según tu preferencia
    const height = 650; // Ajusta la altura según tu preferencia
    const left = (screen.width / 2) - (width / 2);
    const top = (screen.height / 2) - (height / 2);
    const features = `width=${width},height=${height},top=${top},left=${left},resizable=yes,scrollbars=yes,status=yes`;

    // Abre la nueva ventana con las características definidas
    window.open(url, '_blank', features);
    return false; // Previene la acción por defecto del enlace
}
